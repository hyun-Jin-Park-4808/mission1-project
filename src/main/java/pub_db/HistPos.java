package pub_db;

public class HistPos {
	private int histNo;
	private String lnt;
	private String lat;
	private String serDttm;
	
	public int getHistNo() {
		return histNo;
	}
	public void setHistNo(int histNo) {
		this.histNo = histNo;
	}
	public String getLnt() {
		return lnt;
	}
	public void setLnt(String lnt) {
		this.lnt = lnt;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getSerDttm() {
		return serDttm;
	}
	public void setSerDttm(String serDttm) {
		this.serDttm = serDttm;
	}
}
